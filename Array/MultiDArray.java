public class MultiDArray{
    public static void main(String[] args) {
        int a[][] = new int [5][3];
        System.out.println(a[2][1]);
        int b[][] = {
            {1,3,8,5},
            {2,5,4},
            {9,8,3,4,1}
        };
        System.out.println(b[1][1]);

    }
}